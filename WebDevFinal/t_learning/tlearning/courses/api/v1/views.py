from rest_framework import status, viewsets
from rest_framework.response import Response
from courses.models import Category, Course, Enrollment, Lesson, Review, Payment, Quiz, QuizQuestion, UserProgress
from .serializers import CategorySerializer, CourseSerializer, EnrollmentSerializer, LessonSerializer, ReviewSerializer, PaymentSerializer, QuizSerializer, QuizQuestionSerializer, UserProgressSerializer
from rest_framework.decorators import action


# Category ViewSet
class CategoryViewSet(viewsets.ModelViewSet):
    queryset = Category.objects.all()
    serializer_class = CategorySerializer

    def create(self, request, *args, **kwargs):
        serializer = self.get_serializer(data=request.data)
        if serializer.is_valid():
            serializer.save()
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)


